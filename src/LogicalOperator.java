public class LogicalOperator {
    public static void main(String[] args) {
        int a=55;
        int b=65;
        System.out.println(a==b); //false
        System.out.println(a>b); //false
        System.out.println(a<b); //true
        System.out.println(a!=b);  //true
        System.out.println(a<=b);  //true
        System.out.println(a>=b);  //false
    }
}    // final output false false true true true false
